import requests
import markovify
import time
import re


# train the bot on the message file using markovify
def gm_train():
    learning_file = open('gm_msgs.txt', 'r')
    gm_model = markovify.Text(learning_file)
    learning_file.close()
    return gm_model


# send generated message to group
def message(text):
    print(text)
    post_params = {'bot_id': 'YOUR_BOT_ID_HERE', 'text': text}
    requests.post('https://api.groupme.com/v3/bots/post', params=post_params)


# main method that checks for messages
def main():
    arr = []
